#define V 51
#define Inf MAX_INT

int minDistance(int dist[], bool sptSet[]){
    int min = INT_MAX, min_index;

    for (int v = 0; v < V; v++) {
        if (!sptSet[v] && dist[v] <= min){
            min = dist[v];
            min_index = v;
        }
    }
    return min_index;
}

// A utility function to print the distance array and optimal parking space
void printSolution(int dist[], const vector<int>& parkingSpaces){
    cout << "Vertex \t Distance from Source\n";
    vector<pair<int, int>> distances;

    for (int i : parkingSpaces) {
        cout << i << " \t\t\t\t" << dist[i] << endl;
        distances.push_back({dist[i], i});
    }

    // Sort distances to find the optimal space
    sort(distances.begin(), distances.end());

    // Display the optimal parking space
    cout << "Optimal space is lot " << distances[0].second
         << " with the distance of " << distances[0].first << endl;
}

//Djikstra
void dijkstra(int graph[V][V], int src, const vector<int>& parkingSpaces){
    int dist[V];
    bool sptSet[V]; // sptSet[i] will be true if vertex i is included in the shortest path tree

    // Initialize all distances as INFINITE and sptSet[] as false
    for (int i = 0; i < V; i++){
        dist[i] = INT_MAX;
        sptSet[i] = false;
    }

    // Distance of source vertex from itself is always 0
    dist[src] = 0;

    // Find shortest path for all vertices
    for (int count = 0; count < V - 1; count++){
        int u = minDistance(dist, sptSet);

        // Mark the picked vertex as processed
        sptSet[u] = true;

        // Update dist value of the adjacent vertices of the picked vertex
        for (int v = 0; v < V; v++) {
            if (!sptSet[v] && graph[u][v] && dist[u] != INT_MAX && dist[u] + graph[u][v] < dist[v]){
                dist[v] = dist[u] + graph[u][v];
            }
        }
    }

    // Print distances for parking spaces
    printSolution(dist, parkingSpaces);
}

