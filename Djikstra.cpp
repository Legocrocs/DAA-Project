// Finds the minimal total distance from the Djikstra() function
int minDistance(int dist[], bool sptSet[]){
    int min = INT_MAX, min_index;

    for (int v = 0; v < V; v++) {
        if (!sptSet[v] && dist[v] <= min){
            min = dist[v];
            min_index = v;
        }
    }
    return min_index;
}

// Prints the optimal route
void printRoute(int parent[], int j){
    if (parent[j] == -1) {
        cout << j + 1;
        return;
    }
    printRoute(parent, parent[j]);
    cout << " -> " << j + 1;
}

// Self explanatory
void printSolution(int dist[], int parent[], const vector<int>& parkingSpaces){
    vector<pair<int, int>> distances;

    for(int i : parkingSpaces){
        distances.push_back({dist[i], i});
    }
    sort(distances.begin(), distances.end());

    int optimalSpace = distances[0].second;
    cout << "\nOptimal parking space is " << (optimalSpace + 1)
         << " with the distance of " << distances[0].first << endl;

    cout << "Optimal route: ";
    printRoute(parent, optimalSpace);
    cout << endl;
}

void dijkstra(int graph[V][V], int src, int dist[], int parent[], const vector<int>&){
    bool sptSet[V]; 

    for(int i = 0; i < V; i++){
        dist[i] = INT_MAX;
        sptSet[i] = false;
        parent[i] = -1;
    }

    dist[src] = 0;

    for(int count = 0; count < V - 1; count++){
        int u = minDistance(dist, sptSet);

        sptSet[u] = true;

        for(int v = 0; v < V; v++){
            if(!sptSet[v] && graph[u][v] && dist[u] != INT_MAX && dist[u] + graph[u][v] < dist[v]){
                dist[v] = dist[u] + graph[u][v];
                parent[v] = u;
            }
        }
    }
}

